package com.fathor.pondokkopi.Model.Testimonials;

public class TestimonialsData {
    private String name;
    private String image;
    private String tgl_testi;
    private String kesan;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getTgl_testi() {
        return tgl_testi;
    }

    public void setTgl_testi(String tgl_testi) {
        this.tgl_testi = tgl_testi;
    }

    public String getKesan() {
        return kesan;
    }

    public void setKesan(String kesan) {
        this.kesan = kesan;
    }
}
