package com.fathor.pondokkopi.Model.Ongkir;

public class DataOngkir {
    private Integer idkurir;
    private String kurir;
    private String servis;
    private Integer biaya;
    private String estimasi;


    public Integer getIdkurir() {
        return idkurir;
    }

    public void setIdkurir(Integer idkurir) {
        this.idkurir = idkurir;
    }

    public String getKurir() {
        return kurir;
    }

    public void setKurir(String kurir) {
        this.kurir = kurir;
    }

    public String getServis() {
        return servis;
    }

    public void setServis(String servis) {
        this.servis = servis;
    }

    public Integer getBiaya() {
        return biaya;
    }

    public void setBiaya(Integer biaya) {
        this.biaya = biaya;
    }

    public String getEstimasi() {
        return estimasi;
    }

    public void setEstimasi(String estimasi) {
        this.estimasi = estimasi;
    }
}
